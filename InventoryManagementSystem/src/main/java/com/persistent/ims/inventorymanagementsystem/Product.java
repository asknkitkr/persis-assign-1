    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/InventoryDB", "root", "root");
            PreparedStatement add = con.prepareStatement("INSERT INTO PRODUCTTBL VALUES(?,?,?,?,?)");
            add.setInt(1, Integer.valueOf(Product_ID.getText()));
            add.setString(2, Product_Name.getText());
            add.setInt(3, Integer.valueOf(Product_Quantity.getText()));
            add.setString(4, Product_Description.getText());
            add.setString(5, Product_Category.getSelectedItem().toString());
            
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this,"Product Successfully Added");
            con.close();
        } catch(SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_AddBtnMouseClicked

